//Difference between === and ==:
// == => its just check the value are same or not.
//=== => it check the value same or not with its data type.

// Loose equality (==)
10 == 10      // true
10 == '10'    // true (because '10' is converted to 10)

// Strict equality (===)
10 === 10     // true
10 === '10'   // false (because '10' is a string and 10 is a number)








